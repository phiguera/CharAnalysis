% Load both results files
v11 = readmatrix('CO_charResults_V_1_1.csv', 'NumHeaderLines', 1);
v20 = readmatrix('CO_charResults.csv', 'NumHeaderLines', 1);

% ?? Peak count comparison ?????????????????????????????????????????????????
colNames = {'peaks1','peaks2','peaks3','peaksFinal'};
fprintf('\n--- Peak count comparison ---\n')
for c = 1:4
    n11 = nansum(v11(:, 15+c));
    n20 = nansum(v20(:, 15+c));
    fprintf('%s:  v1.1 = %d,  v2.0 = %d,  diff = %d\n', ...
        colNames{c}, n11, n20, n20-n11)
end

% ?? Rows where peaksFinal differs ????????????????????????????????????????
finalCol = 19;
diffRows = find(v11(:,finalCol) ~= v20(:,finalCol));
fprintf('\nRows where peaksFinal differs: %d of %d\n', ...
    length(diffRows), size(v11,1))
if ~isempty(diffRows)
    fprintf('Age (yr BP) at differing rows:\n')
    disp(v11(diffRows, 2))
end

% ?? Threshold comparison ??????????????????????????????????????????????????
fprintf('\n--- Threshold comparison (mean values) ---\n')
threshCols = {'thresh1','thresh2','thresh3','threshFinalPos'};
for c = 1:4
    fprintf('%s:  v1.1 mean = %.4f,  v2.0 mean = %.4f\n', ...
        threshCols{c}, nanmean(v11(:,8+c)), nanmean(v20(:,8+c)))
end

% ?? Threshold time series plot ????????????????????????????????????????????
figure
plot(v11(:,2), v11(:,12), 'b', 'linewidth', 1.5); hold on
plot(v20(:,2), v20(:,12), 'r--', 'linewidth', 1.5);
set(gca, 'xdir', 'reverse')
legend('v1.1 threshFinalPos', 'v2.0 threshFinalPos')
xlabel('age (yr BP)'); ylabel('threshold (# cm-2 yr-1)')
title('Threshold comparison v1.1 vs v2.0')
box off

% ?? Sample-by-sample threshold difference ????????????????????????????????
threshDiff = abs(v11(:,12) - v20(:,12));
fprintf('\n--- Sample-by-sample threshold difference ---\n')
fprintf('Samples where threshold differs: %d of %d\n', ...
    sum(threshDiff > 1e-6), length(threshDiff))
fprintf('Mean threshold difference: %.4f\n', nanmean(threshDiff))
fprintf('Max threshold difference:  %.4f\n', nanmax(threshDiff))

% ?? Peak magnitude comparison ?????????????????????????????????????????????
fprintf('\n--- Peak magnitude comparison ---\n')
magCol = 21;
fprintf('Total peak magnitude:  v1.1 = %.2f,  v2.0 = %.2f\n', ...
    nansum(v11(:,magCol)), nansum(v20(:,magCol)))
fprintf('Mean peak magnitude:   v1.1 = %.4f,  v2.0 = %.4f\n', ...
    nanmean(v11(v11(:,magCol)>0, magCol)), ...
    nanmean(v20(v20(:,magCol)>0, magCol)))

% ?? Fire frequency comparison ?????????????????????????????????????????????
figure
plot(v11(:,2), v11(:,22), 'b', 'linewidth', 1.5); hold on
plot(v20(:,2), v20(:,22), 'r--', 'linewidth', 1.5);
set(gca, 'xdir', 'reverse')
legend('v1.1 smPeakFrequ', 'v2.0 smPeakFrequ')
xlabel('age (yr BP)'); ylabel('fire frequency')
title('Smoothed fire frequency comparison v1.1 vs v2.0')
box off